package entity;

/**
 *
 * @author Andreas
 */
public class Wrote {
    String authorID;
    String bookID;

    public Wrote(String authorID, String bookID) {
        this.authorID = authorID;
        this.bookID = bookID;
    }

    public String getAuthorID() {
        return authorID;
    }

    public void setAuthorID(String authorID) {
        this.authorID = authorID;
    }

    public String getBookID() {
        return bookID;
    }

    public void setBookID(String bookID) {
        this.bookID = bookID;
    }
    
}
